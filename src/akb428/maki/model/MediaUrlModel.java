package akb428.maki.model;

public class MediaUrlModel {

	int id;
	String url;
	String search_word;
	String twitter_user_name;
	String note;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getSearch_word() {
		return search_word;
	}

	public void setSearch_word(String search_word) {
		this.search_word = search_word;
	}

	public String getTwitter_user_name() {
		return twitter_user_name;
	}

	public void setTwitter_user_name(String twitter_user_name) {
		this.twitter_user_name = twitter_user_name;
	}


	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}

}
